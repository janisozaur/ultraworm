/**
 * $Id: Parseable.java,v 1.2 2011/04/18 23:28:06 cix_foo Exp $
 * Parseable things can be decoded from strings. To do this they have to return
 * the name of a class that implements Parser.
 * @author $Author: cix_foo $
 * @version $Revision: 1.2 $
 */
public interface Parseable {

	/**
	 * Decode an object from a String
	 * @param src Source string
	 * @throws Exception
	 */
	public void fromString(String src) throws Exception;

}
